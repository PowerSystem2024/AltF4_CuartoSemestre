#Profundizando en los sitemas de numeracion
#Sistema decimnal
a = 10
print(f"a decimal: {a}")

#Sistema binario
a = 0b1010
print(f"a binario: {a}")

# Sistema octal
a = 0o12
print(f"a octal: {a}")
#Sistema Hexadecimal
a = 0xA
print(f"a Hexadecimal: {a}")
#Base Decimal
a = int('23', 10)
print(f"a = base decimal: {a}")

# Base Binario
a = int('10111',2)
print(f"a base binario : {a}")